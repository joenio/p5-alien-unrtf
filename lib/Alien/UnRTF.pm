package Alien::UnRTF;
use strict;
use warnings;
 
our $VERSION = '0.01';

use parent 'Alien::Base';

=head1 NAME

Alien::UnRTF - Build and make available the unrtf tool

=head1 DESCRIPTION

This distribution installs UnRTF so that it can be used by other Perl
distributions. If already installed for your operating system, and it can be
found, this distribution will use the UnRTF that comes with your operating
system, otherwise it will download it from the Internet, build and install it
from you.

